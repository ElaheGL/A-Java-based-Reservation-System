
public class Bus {
	
	String plate;
	int capacity;

	 public Bus( String pplate , int pcapacity) {
		 this.plate = pplate;
		 this.capacity = pcapacity;
	 }
	 public void setplate(String plate) {
		 this.plate = plate;
	 }
	 public String getplate() {
		 return plate;
	 }
	 public void setcapacity(int capacity) {
		 this.capacity = capacity;
	 }
	 public int getcapacity() {
		 return capacity;
	 }
	

}
